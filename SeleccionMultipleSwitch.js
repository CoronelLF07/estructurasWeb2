let dia = 'sabado';
switch(dia){
    case 'lunes':
        console.log("Inicio de la semana");
        break;
    case 'martes':
        console.log("Segundo Lunes");
        break;
    case 'miercoles':
        console.log("Ombligo de la semana");
        break;
    case 'jueves':
        console.log("Pre viernes");
        break;
    case 'viernes':
        console.log("Hoy toca y el cuerpo lo sabe");
        break;
    default:
        console.log("A descansar")
        break;
}