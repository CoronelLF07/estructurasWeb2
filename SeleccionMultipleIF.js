let dia = "lunes";

if(dia == 'lunes'){
    console.log("Inicio de semana");
}else if(dia == 'martes'){
    console.log("Segundo Lunes!!");
}else if(dia == 'miercoles'){
    console.log("Ombligo de la semana");
}else if(dia == 'jueves'){
    console.log("Precopeo");
}else if(dia == 'viernes'){
    console.log("Hoy toca");
}else{
    console.log("A descansar");
}
console.log("Fin")